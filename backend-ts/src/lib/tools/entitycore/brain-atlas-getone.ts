import { z } from 'zod';

import { BaseTool, type EntitycoreContextVariables } from '../base-tool';
import { zBrainAtlasRead } from '../autogenerated_types/entitycore/zod.gen';

/**
 * Input schema for Brain Atlas GetOne tool
 *
 * Requires a brain_atlas_id to retrieve specific brain atlas details.
 *
 * Equivalent to Python's BrainAtlasGetOneInput
 */
const BrainAtlasGetOneInputSchema = z.object({
  brain_atlas_id: z
    .string()
    .uuid()
    .describe('ID of the brain atlas of interest in UUID format.'),
});

/**
 * Brain Atlas GetOne Tool
 *
 * Retrieves detailed information about a specific brain atlas from the EntityCore knowledge graph.
 */
export class BrainAtlasGetOneTool extends BaseTool<
  typeof BrainAtlasGetOneInputSchema,
  EntitycoreContextVariables
> {
  // Static properties (tool metadata)
  static readonly toolName = 'entitycore-brainatlas-getone';
  static readonly toolNameFrontend = 'Get One Brain Atlas';
  static readonly toolDescription = `Retrieves detailed information about a specific brain atlas from the knowledge graph.
Requires a 'brain_atlas_id' which is the ID of the brain atlas of interest as registered in the knowledge graph.
The output contains detailed information about the brain atlas, including:
- The brain atlas ID
- The brain atlas name
- The brain atlas species
- Creation and update dates`;
  static readonly toolDescriptionFrontend = `Get detailed information about a specific brain atlas. Use this tool to:
• View complete brain atlas details
• Access brain atlas name and species
• Get creation and update information

Specify the brain atlas ID to retrieve its full details.`;
  static readonly toolUtterances = [
    'Get details for this brain atlas',
    'Show me information about this atlas',
    'What are the properties of this brain atlas?',
  ];
  static readonly toolHil = false;

  override contextVariables: EntitycoreContextVariables;
  override inputSchema = BrainAtlasGetOneInputSchema;

  constructor(contextVariables: EntitycoreContextVariables) {
    super();
    this.contextVariables = contextVariables;
  }

  /**
   * Execute the brain atlas retrieval
   *
   * @param input - Validated input with brain_atlas_id
   * @returns Detailed brain atlas information
   */
  async execute(
    input: z.infer<typeof BrainAtlasGetOneInputSchema>
  ): Promise<z.infer<typeof zBrainAtlasRead>> {
    const { entitycoreUrl, vlabId, projectId, httpClient } = this.contextVariables;

    // Prepare headers
    const headers: Record<string, string> = {};
    if (vlabId) {
      headers['virtual-lab-id'] = vlabId;
    }
    if (projectId) {
      headers['project-id'] = projectId;
    }

    try {
      const url = `${entitycoreUrl.replace(/\/$/, '')}/brain-atlas/${input.brain_atlas_id}`;

      // Use ky's built-in JSON parsing and error handling
      const data = await httpClient
        .get(url, {
          headers,
        })
        .json();

      // Validate response with Zod schema
      const validated = zBrainAtlasRead.parse(data);
      return validated;
    } catch (error: any) {
      throw new Error(
        `The brain atlas endpoint returned a non 200 response code. Error: ${error.message || JSON.stringify(error)}`
      );
    }
  }

  /**
   * Static health check method (classmethod equivalent)
   *
   * @param contextVariables - Context variables with API configuration
   * @returns True if the service is online
   */
  static async isOnline(contextVariables: EntitycoreContextVariables): Promise<boolean> {
    try {
      const { entitycoreUrl, httpClient } = contextVariables;
      const healthUrl = `${entitycoreUrl.replace(/\/$/, '')}/health`;

      await httpClient.get(healthUrl);
      return true;
    } catch {
      return false;
    }
  }
}

import { z } from 'zod';

import { BaseTool, type EntitycoreContextVariables } from '../base-tool';
import { zEntityRoute, zAssetRead } from '../autogenerated_types/entitycore/zod.gen';

/**
 * Input schema for Asset GetOne tool
 *
 * Requires entity_route, entity_id, and asset_id to retrieve a specific asset.
 *
 * Equivalent to Python's AssetGetOneInputSchema
 */
const AssetGetOneInputSchema = z.object({
  entity_route: zEntityRoute.describe('The route of the entity'),
  entity_id: z.string().uuid().describe('The ID of the entity'),
  asset_id: z.string().uuid().describe('The ID of the asset'),
});

/**
 * Asset GetOne Tool
 *
 * Retrieves detailed information about a specific asset from the EntityCore service.
 */
export class AssetGetOneTool extends BaseTool<
  typeof AssetGetOneInputSchema,
  EntitycoreContextVariables
> {
  // Static properties (tool metadata)
  static readonly toolName = 'entitycore-asset-getone';
  static readonly toolNameFrontend = 'Get One Asset';
  static readonly toolDescription = `Retrieves a single asset from the entitycore service.
The output contains:
- The asset path
- The asset full path
- Whether it's a directory
- Content type
- Size
- SHA256 digest
- Metadata
- Label
- ID
- Status`;
  static readonly toolDescriptionFrontend = `Retrieve a single asset. Use this tool to:
• Get detailed information about a specific asset
• Access asset metadata and properties

Specify the entity route and entity ID to retrieve the asset.`;
  static readonly toolUtterances = [
    'Get details for this asset',
    'Show me information about this file',
    'What are the properties of this asset?',
  ];
  static readonly toolHil = false;

  override contextVariables: EntitycoreContextVariables;
  override inputSchema = AssetGetOneInputSchema;

  constructor(contextVariables: EntitycoreContextVariables) {
    super();
    this.contextVariables = contextVariables;
  }

  /**
   * Execute the asset retrieval
   *
   * @param input - Validated input with entity_route, entity_id, and asset_id
   * @returns Detailed asset information
   */
  async execute(
    input: z.infer<typeof AssetGetOneInputSchema>
  ): Promise<z.infer<typeof zAssetRead>> {
    const { entitycoreUrl, vlabId, projectId, httpClient } = this.contextVariables;

    // Prepare headers
    const headers: Record<string, string> = {};
    if (vlabId) {
      headers['virtual-lab-id'] = vlabId;
    }
    if (projectId) {
      headers['project-id'] = projectId;
    }

    try {
      const url = `${entitycoreUrl.replace(/\/$/, '')}/${input.entity_route}/${input.entity_id}/assets/${input.asset_id}`;

      // Use ky's built-in JSON parsing and error handling
      const data = await httpClient
        .get(url, {
          headers,
        })
        .json();

      // Validate response with Zod schema
      const validated = zAssetRead.parse(data);
      return validated;
    } catch (error: any) {
      throw new Error(
        `The asset endpoint returned a non 200 response code. Error: ${error.message || JSON.stringify(error)}`
      );
    }
  }

  /**
   * Static health check method (classmethod equivalent)
   *
   * @param contextVariables - Context variables with API configuration
   * @returns True if the service is online
   */
  static async isOnline(contextVariables: EntitycoreContextVariables): Promise<boolean> {
    try {
      const { entitycoreUrl, httpClient } = contextVariables;
      const healthUrl = `${entitycoreUrl.replace(/\/$/, '')}/health`;

      await httpClient.get(healthUrl);
      return true;
    } catch {
      return false;
    }
  }
}

import { z } from 'zod';
import { HTTPError } from 'ky';

import { BaseTool, type EntitycoreContextVariables } from '../base-tool';
import { zStrainRead } from '../autogenerated_types/entitycore/zod.gen';

/**
 * Input schema for Strain GetOne tool
 *
 * Requires a strain_id to retrieve detailed information about a specific strain.
 */
const StrainGetOneInputSchema = z.object({
  strain_id: z.string().uuid().describe('ID of the strain of interest in UUID format.'),
});

/**
 * Strain GetOne Tool
 *
 * Retrieves detailed information about a specific strain from the EntityCore knowledge graph.
 */
export class StrainGetOneTool extends BaseTool<
  typeof StrainGetOneInputSchema,
  EntitycoreContextVariables
> {
  // Static properties (tool metadata)
  static readonly toolName = 'entitycore-strain-getone';
  static readonly toolNameFrontend = 'Get One Strain';
  static readonly toolDescription = `Retrieves detailed information about a specific strain from the knowledge graph.
Requires a 'strain_id' which is the ID of the strain of interest as registered in the knowledge graph.
The output contains detailed information about the strain, including:
- The strain ID
- The strain name
- The strain taxonomy ID
- The species ID
- Creation and update dates`;
  static readonly toolDescriptionFrontend = `Get detailed information about a specific strain. Use this tool to:
• View complete strain details
• Access strain name and taxonomy information
• Get creation and update information

Specify the strain ID to retrieve its full details.`;
  static readonly toolUtterances = [
    'Get details for this strain',
    'Show me information about this strain',
    'What are the properties of this strain?',
  ];

  override contextVariables: EntitycoreContextVariables;
  override inputSchema = StrainGetOneInputSchema;

  constructor(contextVariables: EntitycoreContextVariables) {
    super();
    this.contextVariables = contextVariables;
  }

  /**
   * Execute the strain retrieval
   *
   * @param input - Validated input with strain_id
   * @returns Detailed strain information
   */
  async execute(
    input: z.infer<typeof StrainGetOneInputSchema>
  ): Promise<z.infer<typeof zStrainRead>> {
    const { entitycoreUrl, vlabId, projectId, httpClient } = this.contextVariables;

    // Prepare headers
    const headers: Record<string, string> = {};
    if (vlabId) {
      headers['virtual-lab-id'] = vlabId;
    }
    if (projectId) {
      headers['project-id'] = projectId;
    }

    try {
      const url = `${entitycoreUrl.replace(/\/$/, '')}/strain/${input.strain_id}`;

      // Use ky's built-in JSON parsing and error handling
      const data = await httpClient
        .get(url, {
          headers,
        })
        .json<any>();

      // Validate response with Zod schema
      const validated = zStrainRead.parse(data);
      return validated;
    } catch (error) {
      if (error instanceof HTTPError) {
        const { status, statusText } = error.response;
        let responseBody = '';
        try {
          responseBody = await error.response.text();
        } catch {
          // Ignore if we can't read the body
        }
        throw new Error(
          `The strain endpoint returned a non 200 response code. Error: ${status} ${statusText}${responseBody ? ': ' + responseBody : ''}`
        );
      }
      throw error;
    }
  }

  /**
   * Static health check method (classmethod equivalent)
   *
   * @param contextVariables - Context variables with API configuration
   * @returns True if the service is online
   */
  static async isOnline(contextVariables: EntitycoreContextVariables): Promise<boolean> {
    try {
      const { entitycoreUrl, httpClient } = contextVariables;
      const healthUrl = `${entitycoreUrl.replace(/\/$/, '')}/health`;

      await httpClient.get(healthUrl);
      return true;
    } catch {
      return false;
    }
  }
}

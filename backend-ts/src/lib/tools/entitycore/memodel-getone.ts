import { z } from 'zod';
import { HTTPError } from 'ky';

import { BaseTool, type EntitycoreContextVariables } from '../base-tool';
import { zReadOneMemodelIdGetResponse } from '../autogenerated_types/entitycore/zod.gen';

/**
 * Input schema for MEModel GetOne tool
 *
 * Requires a me_model_id to retrieve detailed information about a specific ME-Model.
 */
const MEModelGetOneInputSchema = z.object({
  me_model_id: z
    .string()
    .uuid()
    .describe('ID of the ME-Model of interest in UUID format.'),
});

/**
 * MEModel GetOne Tool
 *
 * Retrieves detailed information about a specific ME-Model from the EntityCore knowledge graph.
 */
export class MEModelGetOneTool extends BaseTool<
  typeof MEModelGetOneInputSchema,
  EntitycoreContextVariables
> {
  // Static properties (tool metadata)
  static readonly toolName = 'entitycore-memodel-getone';
  static readonly toolNameFrontend = 'Get One ME-Model';
  static readonly toolDescription = `Retrieve a single ME-Model by its UUID from the EntityCore service. Retrieves more data than the correspoding get all endpoint.`;
  static readonly toolDescriptionFrontend = `Fetch a specific ME-Model using its unique ID. Useful for accessing detailed ME-Model data.`;
  static readonly toolUtterances = [
    'Get details for this ME-model',
    'Show me information about this ME-model',
    'What are the properties of this ME-model?',
  ];
  static readonly toolHil = false;

  override contextVariables: EntitycoreContextVariables;
  override inputSchema = MEModelGetOneInputSchema;

  constructor(contextVariables: EntitycoreContextVariables) {
    super();
    this.contextVariables = contextVariables;
  }

  /**
   * Execute the ME-Model retrieval
   *
   * @param input - Validated input with me_model_id
   * @returns Detailed ME-Model information
   */
  async execute(
    input: z.infer<typeof MEModelGetOneInputSchema>
  ): Promise<z.infer<typeof zReadOneMemodelIdGetResponse>> {
    const { entitycoreUrl, vlabId, projectId, httpClient, entityFrontendUrl } =
      this.contextVariables;

    // Prepare headers
    const headers: Record<string, string> = {};
    if (vlabId) {
      headers['virtual-lab-id'] = vlabId;
    }
    if (projectId) {
      headers['project-id'] = projectId;
    }

    try {
      const url = `${entitycoreUrl.replace(/\/$/, '')}/memodel/${input.me_model_id}`;

      // Use ky's built-in JSON parsing and error handling
      const data = await httpClient
        .get(url, {
          headers,
        })
        .json<any>();

      // Add the link to website
      data.url_link = `${entityFrontendUrl}/${data.id}`;

      // Validate response with Zod schema
      const validated = zReadOneMemodelIdGetResponse.parse(data);
      return validated;
    } catch (error) {
      if (error instanceof HTTPError) {
        const { status, statusText } = error.response;
        let responseBody = '';
        try {
          responseBody = await error.response.text();
        } catch {
          // Ignore if we can't read the body
        }
        throw new Error(
          `The ME-Model endpoint returned a non 200 response code. Error: ${status} ${statusText}${responseBody ? ': ' + responseBody : ''}`
        );
      }
      throw error;
    }
  }

  /**
   * Static health check method (classmethod equivalent)
   *
   * @param contextVariables - Context variables with API configuration
   * @returns True if the service is online
   */
  static async isOnline(contextVariables: EntitycoreContextVariables): Promise<boolean> {
    try {
      const { entitycoreUrl, httpClient } = contextVariables;
      const healthUrl = `${entitycoreUrl.replace(/\/$/, '')}/health`;

      await httpClient.get(healthUrl);
      return true;
    } catch {
      return false;
    }
  }
}

"""Get One Brain Region Hierarchy tool."""

from typing import ClassVar
from uuid import UUID

from httpx import AsyncClient
from pydantic import BaseModel, Field

from neuroagent.tools.autogenerated_types.entitycore import (
    BrainRegionHierarchyRead,
)
from neuroagent.tools.base_tool import BaseMetadata, BaseTool


class BrainRegionHierarchyGetOneInput(BaseModel):
    """Inputs of the knowledge graph API."""

    hierarchy_id: str = Field(
        description="ID of the brain region hierarchy of interest in UUID format."
    )


class BrainRegionHierarchyGetOneMetadata(BaseMetadata):
    """Metadata class for BrainRegionHierarchyGetOneTool."""

    httpx_client: AsyncClient
    entitycore_url: str
    vlab_id: str | None
    project_id: str | None


class BrainRegionHierarchyGetOneTool(BaseTool):
    """Class defining the Get One Brain Region Hierarchy logic."""

    name: ClassVar[str] = "entitycore-brainregionhierarchy-getone"
    name_frontend: ClassVar[str] = "Get One Brain Region Hierarchy"
    description: ClassVar[
        str
    ] = """Retrieves detailed information about a specific brain region hierarchy from the knowledge graph.
    Requires a 'hierarchy_id' which is the ID of the brain region hierarchy of interest as registered in the knowledge graph.
    The output contains detailed information about the hierarchy, including:
    - The hierarchy ID
    - The hierarchy name
    - Creation and update dates
    """
    description_frontend: ClassVar[
        str
    ] = """Get detailed information about a specific brain region hierarchy. Use this tool to:
    • View complete hierarchy details
    • Access hierarchy name
    • Get creation and update information

    Specify the hierarchy ID to retrieve its full details."""
    metadata: BrainRegionHierarchyGetOneMetadata
    input_schema: BrainRegionHierarchyGetOneInput

    async def arun(self) -> BrainRegionHierarchyRead:
        """From a hierarchy ID, extract detailed brain region hierarchy information.

        Returns
        -------
            BrainRegionHierarchyRead containing detailed hierarchy information, or an error dict.
        """
        # Validate the UUID format
        hierarchy_id = UUID(self.input_schema.hierarchy_id)

        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = self.metadata.vlab_id
        if self.metadata.project_id is not None:
            headers["project-id"] = self.metadata.project_id

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/")
            + f"/brain-region-hierarchy/{hierarchy_id}",
            headers=headers,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The brain region hierarchy endpoint returned a non 200 response code. Error: {response.text}"
            )
        return BrainRegionHierarchyRead(**response.json())

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

"""Get One Simulation tool."""

from typing import ClassVar
from uuid import UUID

from httpx import AsyncClient
from pydantic import BaseModel, Field

from neuroagent.tools.autogenerated_types.entitycore import (
    SimulationRead,
)
from neuroagent.tools.base_tool import BaseTool, EntitycoreMetadata


class SimulationGetOneInput(BaseModel):
    """Inputs for the simulation get one tool."""

    simulation_id: UUID = Field(
        description="ID of the simulation of interest in UUID format."
    )


class SimulationGetOneTool(BaseTool):
    """Class defining the Get One Simulation logic."""

    name: ClassVar[str] = "entitycore-simulation-getone"
    name_frontend: ClassVar[str] = "Get One Simulation"
    description: ClassVar[
        str
    ] = """Retrieves detailed information about a specific simulation from the knowledge graph.
    Requires a 'simulation_id' which is the ID of the simulation of interest as registered in the knowledge graph.
    The output contains detailed information about the simulation, including:
    - The simulation ID and basic identifiers
    - Core simulation information and properties
    - Simulation parameters and configuration
    - Creation and modification timestamps
    - Creation and update dates
    """
    description_frontend: ClassVar[
        str
    ] = """Get detailed information about a specific simulation. Use this tool to:
    • View complete simulation details
    • Access detailed simulation information
    • Get creation and update information

    Specify the simulation ID to retrieve its full details."""
    metadata: EntitycoreMetadata
    input_schema: SimulationGetOneInput

    async def arun(self) -> SimulationRead:
        """From a simulation ID, extract detailed simulation information.

        Returns
        -------
            SimulationRead containing detailed simulation information, or an error dict.
        """
        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = str(self.metadata.vlab_id)
        if self.metadata.project_id is not None:
            headers["project-id"] = str(self.metadata.project_id)

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/")
            + f"/simulation/{self.input_schema.simulation_id}",
            headers=headers,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The simulation endpoint returned a non 200 response code. Error: {response.text}"
            )
        return SimulationRead(**response.json())

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

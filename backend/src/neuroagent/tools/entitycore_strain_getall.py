"""Get All Strains tool."""

from typing import ClassVar

from httpx import AsyncClient
from pydantic import Field
from pydantic.json_schema import SkipJsonSchema

from neuroagent.tools.autogenerated_types.entitycore import (
    ListResponseStrainRead,
    ReadManyStrainGetParametersQuery,
)
from neuroagent.tools.base_tool import BaseTool, EntitycoreMetadata


class StrainGetAllInput(ReadManyStrainGetParametersQuery):
    """Inputs for the strain get all tool."""

    page_size: int = Field(
        ge=1,
        le=10,
        default=5,
        description="Number of items per page",
    )
    semantic_search: str | None = Field(
        default=None,
        title="Strain Name Search",
        description="Perform semantic search to find strains by their names. Enter any text related to a strain name and receive results ranked by semantic similarity to your query.",
    )
    name: SkipJsonSchema[None] = Field(default=None, exclude=True)
    name__in: SkipJsonSchema[None] = Field(default=None, exclude=True)
    name__ilike: SkipJsonSchema[None] = Field(default=None, exclude=True)


class StrainGetAllTool(BaseTool):
    """Class defining the Get All Strain logic."""

    name: ClassVar[str] = "entitycore-strain-getall"
    name_frontend: ClassVar[str] = "Get All Strains"
    utterances: ClassVar[list[str]] = [
        "Find strains",
        "Show me available strains",
        "What strains are there?",
    ]
    description: ClassVar[
        str
    ] = """Searches the entitycore database to retrieve strains.
    The output is a list of strains, containing:
    - The strain ID
    - The strain name
    - The strain description
    - Any additional metadata

    Note: The `semantic_search` parameter will always return results even for irrelevant queries - critically evaluate whether returned strains are actually related to your search terms.
    """
    description_frontend: ClassVar[
        str
    ] = """Search and retrieve strains. Use this tool to:
    • Find strains by name
    • Access detailed strain data

    Specify optional criteria to find relevant strains."""
    metadata: EntitycoreMetadata
    input_schema: StrainGetAllInput

    async def arun(self) -> ListResponseStrainRead:
        """Extract strains.

        Returns
        -------
            list of strains and their metadata, or an error dict.
        """
        query_params = self.input_schema.model_dump(exclude_defaults=True, mode="json")
        query_params["page_size"] = self.input_schema.page_size

        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = str(self.metadata.vlab_id)
        if self.metadata.project_id is not None:
            headers["project-id"] = str(self.metadata.project_id)

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/") + "/strain",
            headers=headers,
            params=query_params,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The strain endpoint returned a non 200 response code. Error: {response.text}"
            )

        response_data = response.json()
        return ListResponseStrainRead(**response_data)

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

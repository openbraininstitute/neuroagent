"""Get All Cell Morphologies tool."""

from typing import ClassVar
from uuid import UUID

from httpx import AsyncClient
from pydantic import Field

from neuroagent.tools.autogenerated_types.entitycore import (
    ListResponseCellMorphologyRead,
    ReadManyCellMorphologyGetParametersQuery,
    WithinBrainRegionDirection,
)
from neuroagent.tools.base_tool import (
    BaseTool,
    EntitycoreExcludeBRParams,
    EntitycoreMetadata,
)


class CellMorphologyGetAllInput(
    EntitycoreExcludeBRParams, ReadManyCellMorphologyGetParametersQuery
):
    """Inputs for the cell morphology get all tool."""

    within_brain_region_brain_region_id: UUID | None = Field(
        default=None,
        description="ID of the brain region of interest in UUID format.",
    )
    page_size: int = Field(
        ge=1,
        le=10,
        default=5,
        description="Number of items per page",
    )
    within_brain_region_direction: WithinBrainRegionDirection = Field(
        default="ascendants_and_descendants", title="Within Brain Region Direction"
    )


class CellMorphologyGetAllTool(BaseTool):
    """Class defining the Get All Cell Morphology logic."""

    name: ClassVar[str] = "entitycore-cellmorphology-getall"
    name_frontend: ClassVar[str] = "Get All Cell Morphologies"
    utterances: ClassVar[list[str]] = [
        "Find a morphology in the isocortex and give me its features",
        "Find cell morphologies",
        "Show me available cell morphologies",
        "What cell morphologies are there?",
    ]
    description: ClassVar[
        str
    ] = """Searches the entitycore database to retrieve cell morphologies.
    The output is a list of cell morphologies, containing:
    - The brain region ID.
    - The brain region name.
    - The subject species name.
    - The cell morphology ID.
    - The cell morphology name.
    - The cell morphology description.
    - The measurements.
    - The mtypes.
    - Any additional metadata.

    Leaving all query parameters blank performs a broad search.
    """
    description_frontend: ClassVar[
        str
    ] = """Search and retrieve cell morphologies. Use this tool to:
    • Find morphologies in specific brain regions
    • Filter by subject and species
    • Access detailed morphology data

    Specify brain region and optional criteria to find relevant morphologies."""
    metadata: EntitycoreMetadata
    input_schema: CellMorphologyGetAllInput

    async def arun(self) -> ListResponseCellMorphologyRead:
        """From a brain region ID, extract morphologies.

        Returns
        -------
            ListResponseCellMorphologyRead describing the morphology and its metadata.
        """
        query_params = self.input_schema.model_dump(exclude_defaults=True, mode="json")
        query_params["page_size"] = self.input_schema.page_size

        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = str(self.metadata.vlab_id)
        if self.metadata.project_id is not None:
            headers["project-id"] = str(self.metadata.project_id)

        # Retrieve the hierarchy_id from br_id
        if self.input_schema.within_brain_region_brain_region_id is not None:
            brain_region = await self.metadata.httpx_client.get(
                url=self.metadata.entitycore_url.rstrip("/")
                + f"/brain-region/{self.input_schema.within_brain_region_brain_region_id}",
                headers=headers,
            )
            if brain_region.status_code != 200:
                raise ValueError(
                    f"The Brain Region endpoint returned a non 200 response code. Error: {brain_region.text}"
                )
            query_params["within_brain_region_hierarchy_id"] = brain_region.json()[
                "hierarchy_id"
            ]

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/") + "/cell-morphology",
            headers=headers,
            params=query_params,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The morphology endpoint returned a non 200 response code. Error: {response.text}"
            )

        response_data = response.json()
        # Set assets and legacy_id to empty lists for each morphology
        for morphology in response_data["data"]:
            morphology["legacy_id"] = []

            # Add the links to website
            morphology["url_link"] = (
                self.metadata.entity_frontend_url + "/" + morphology["id"]
            )

        return ListResponseCellMorphologyRead(**response_data)

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

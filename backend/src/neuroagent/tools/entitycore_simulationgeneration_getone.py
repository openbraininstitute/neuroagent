"""Get One SimulationGeneration tool."""

from typing import ClassVar
from uuid import UUID

from httpx import AsyncClient
from pydantic import BaseModel, Field

from neuroagent.autogenerated_types.entitycore import (
    SimulationGenerationRead,
)
from neuroagent.tools.base_tool import BaseTool, EntitycoreMetadata


class SimulationGenerationGetOneInput(BaseModel):
    """Inputs for the simulation-generation get one tool."""

    simulation_generation_id: UUID = Field(
        description="ID of the simulation-generation of interest in UUID format."
    )


class SimulationGenerationGetOneTool(BaseTool):
    """Class defining the Get One SimulationGeneration logic."""

    name: ClassVar[str] = "entitycore-simulationgeneration-getone"
    name_frontend: ClassVar[str] = "Get One Simulation Generation"
    utterances: ClassVar[list[str]] = [
        "Get details for this simulation generation",
        "Show me information about this simulation generation",
        "What are the properties of this simulation generation?",
    ]
    description: ClassVar[
        str
    ] = """Retrieves detailed information about a specific simulation-generation from the knowledge graph.
    Requires a 'simulation_generation_id' which is the ID of the simulation-generation of interest as registered in the knowledge graph.
    The output contains detailed information about the simulation-generation, including:
    - The simulation generation ID and basic identifiers
    - Core simulation generation information and properties
    - Simulation parameters and configuration
    - Creation and modification timestamps
    - Creation and update dates
    """
    description_frontend: ClassVar[
        str
    ] = """Get detailed information about a specific simulation-generation. Use this tool to:
    • View complete simulation-generation details
    • Access detailed simulation-generation information
    • Get creation and update information

    Specify the simulation-generation ID to retrieve its full details."""
    metadata: EntitycoreMetadata
    input_schema: SimulationGenerationGetOneInput

    async def arun(self) -> SimulationGenerationRead:
        """From a simulation-generation ID, extract detailed simulation-generation information.

        Returns
        -------
            SimulationGenerationRead containing detailed simulation-generation information, or an error dict.
        """
        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = str(self.metadata.vlab_id)
        if self.metadata.project_id is not None:
            headers["project-id"] = str(self.metadata.project_id)

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/")
            + f"/simulation-generation/{self.input_schema.simulation_generation_id}",
            headers=headers,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The simulation-generation endpoint returned a non 200 response code. Error: {response.text}"
            )
        return SimulationGenerationRead(**response.json())

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

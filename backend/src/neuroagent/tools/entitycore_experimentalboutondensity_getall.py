"""Get All Experimental Bouton Densities tool."""

from typing import ClassVar
from uuid import UUID

from httpx import AsyncClient
from pydantic import Field
from pydantic.json_schema import SkipJsonSchema

from neuroagent.tools.autogenerated_types.entitycore import (
    ListResponseExperimentalBoutonDensityRead,
    ReadManyExperimentalBoutonDensityGetParametersQuery,
)
from neuroagent.tools.base_tool import BaseTool, EntitycoreMetadata


class ExperimentalBoutonDensityGetAllInput(
    ReadManyExperimentalBoutonDensityGetParametersQuery
):
    """Inputs for the experimental bouton density get all tool."""

    brain_region__id: SkipJsonSchema[None] = Field(default=None, exclude=True)
    brain_region__id__in: SkipJsonSchema[None] = Field(default=None, exclude=True)
    within_brain_region_hierarchy_id: UUID | None = Field(
        default=UUID("e3e70682-c209-4cac-a29f-6fbed82c07cd"),
        description="The hierarchy ID for brain regions. The default value is the most commonly used hierarchy ID.",
    )
    within_brain_region_brain_region_id: UUID | None = Field(
        default=None,
        description="ID of the brain region of interest in UUID format.",
    )
    page_size: int = Field(
        ge=1,
        le=10,
        default=5,
        description="Number of items per page",
    )


class ExperimentalBoutonDensityGetAllTool(BaseTool):
    """Class defining the Get All Experimental Bouton Densities logic."""

    name: ClassVar[str] = "entitycore-experimentalboutondensity-getall"
    name_frontend: ClassVar[str] = "Get All Experimental Bouton Densities"
    description: ClassVar[
        str
    ] = """Searches the entitycore database to retrieve experimental bouton densities.
    The output is a list of experimental bouton densities, containing:
    - The brain region ID.
    - The brain region name.
    - The subject species name.
    - The bouton density ID.
    - The bouton density name.
    - The bouton density description.
    - The measurements.
    - The mtypes.
    - Any additional metadata.

    We explicitly exclude the assets and the contributions but you can access them using the Get One Experimental Bouton Density tool.
    """
    description_frontend: ClassVar[
        str
    ] = """Search and retrieve experimental bouton densities. Use this tool to:
    • Find densities in specific brain regions
    • Filter by subject and species
    • Access detailed density data

    Specify brain region and optional criteria to find relevant densities."""
    metadata: EntitycoreMetadata
    input_schema: ExperimentalBoutonDensityGetAllInput

    async def arun(self) -> ListResponseExperimentalBoutonDensityRead:
        """From a brain region ID, extract experimental bouton densities.

        Returns
        -------
            list of ExperimentalBoutonDensityRead to describe the densities and their metadata, or an error dict.
        """
        query_params = self.input_schema.model_dump(exclude_defaults=True, mode="json")
        query_params["page_size"] = self.input_schema.page_size
        query_params["within_brain_region_hierarchy_id"] = (
            self.input_schema.within_brain_region_hierarchy_id
        )

        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = self.metadata.vlab_id
        if self.metadata.project_id is not None:
            headers["project-id"] = self.metadata.project_id

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/")
            + "/experimental-bouton-density",
            headers=headers,
            params=query_params,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The experimental bouton density endpoint returned a non 200 response code. Error: {response.text}"
            )

        response_data = response.json()
        # Set assets and contributions to empty lists for each density
        for density in response_data["data"]:
            density["assets"] = []
            density["contributions"] = []

        return ListResponseExperimentalBoutonDensityRead(**response_data)

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

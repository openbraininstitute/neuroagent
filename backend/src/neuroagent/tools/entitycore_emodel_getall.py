"""Get EModel tool."""

import logging
from typing import ClassVar
from uuid import UUID

from httpx import AsyncClient
from pydantic import BaseModel, Field
from pydantic.json_schema import SkipJsonSchema

from neuroagent.tools.autogenerated_types.entitycore.models import (
    ListResponseEModelReadExpanded,
)
from neuroagent.tools.autogenerated_types.entitycore.schemas import (
    ReadManyEmodelGetParams,
)
from neuroagent.tools.base_tool import BaseMetadata, BaseTool

logger = logging.getLogger(__name__)


class EModelGetAllInput(BaseModel):
    """Inputs of the knowledge graph API."""

    page_size: int = Field(default=100, description="Number of results per page.")
    page: int = Field(default=1, description="Page number for pagination.")
    name: str | None = Field(
        default=None,
        description="Name of the e-model to filter by.",
    )
    brain_region_id: str | None = Field(
        default=None,
        description="ID of the brain region to filter by in UUID format.",
    )
    species_id: str | None = Field(
        default=None,
        description="ID of the species to filter by in UUID format.",
    )


class EModelGetAllMetadata(BaseMetadata):
    """Metadata class for EModelGetAllTool."""

    httpx_client: AsyncClient
    entitycore_url: str
    vlab_id: str | None
    project_id: str | None


class EModelGetAllQueryParams(ReadManyEmodelGetParams):
    """Query parameters for EModelGetAllTool with skipped JSON schema for certain fields."""

    virtual_lab_id: SkipJsonSchema[None] = None
    project_id: SkipJsonSchema[None] = None


class EModelGetAllTool(BaseTool):
    """Class defining the Get EModel logic."""

    name: ClassVar[str] = "entitycore-emodel-getall"
    name_frontend: ClassVar[str] = "Get All E-Models"
    description: ClassVar[
        str
    ] = """Searches a neuroscience based knowledge graph to retrieve e-models.
    The output is a list of e-models, containing:
    - The e-model ID
    - The e-model name
    - The e-model description
    - The e-model iteration
    - The e-model score
    - The associated species
    - The associated brain region
    - The associated e-types and m-types
    - Creation and update dates

    We explicitly exclude the assets and ion channel models but you can access them using the Get One E-Model tool.
    """
    description_frontend: ClassVar[
        str
    ] = """Search and retrieve e-models. Use this tool to:
    • Find e-models by name
    • Filter by brain region
    • Filter by species
    • Access detailed e-model data

    Specify optional criteria to find relevant e-models."""
    metadata: EModelGetAllMetadata
    input_schema: EModelGetAllInput

    async def arun(self) -> ListResponseEModelReadExpanded:
        """Extract e-models.

        Returns
        -------
            list of e-models and their metadata, or an error dict.
        """
        logger.info(
            f"Entering Get EModel tool. Inputs: {self.input_schema.model_dump()}"
        )

        query_params = EModelGetAllQueryParams(
            page_size=self.input_schema.page_size,
            page=self.input_schema.page,
            name=self.input_schema.name,
            brain_region__id=UUID(self.input_schema.brain_region_id)
            if self.input_schema.brain_region_id
            else None,
            species__id=UUID(self.input_schema.species_id)
            if self.input_schema.species_id
            else None,
        )

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/") + "/emodel",
            headers={
                **(
                    {"virtual-lab-id": self.metadata.vlab_id}
                    if self.metadata.vlab_id is not None
                    else {}
                ),
                **(
                    {"project-id": self.metadata.project_id}
                    if self.metadata.project_id is not None
                    else {}
                ),
            },
            params=query_params.model_dump(exclude_defaults=True),
        )
        if response.status_code != 200:
            raise ValueError(
                f"The emodel endpoint returned a non 200 response code. Error: {response.text}"
            )

        response_data = response.json()
        # Set assets and ion_channel_models to empty list for each emodel
        for emodel in response_data["data"]:
            emodel["assets"] = []
            emodel["ion_channel_models"] = []

        return ListResponseEModelReadExpanded(**response_data)

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

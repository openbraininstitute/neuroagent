"""Get EModel tool."""

from typing import ClassVar, Literal
from uuid import UUID

from httpx import AsyncClient
from pydantic import Field

from neuroagent.autogenerated_types.entitycore import (
    ListResponseEModelReadExpanded,
    ReadManyEmodelGetParametersQuery,
)
from neuroagent.tools.base_tool import (
    BaseTool,
    EntitycoreExcludeBRParams,
    EntitycoreMetadata,
)


class EModelGetAllInput(EntitycoreExcludeBRParams, ReadManyEmodelGetParametersQuery):
    """Inputs for the e-model get all tool."""

    within_brain_region_brain_region_id: UUID | None = Field(
        default=None,
        description="ID of the brain region of interest in UUID format.",
    )
    page_size: int = Field(
        ge=1,
        le=10,
        default=5,
        description="Number of items per page",
    )
    within_brain_region_direction: Literal[
        "ascendants", "descendants", "ascendants_and_descendants"
    ] = Field(  # type: ignore[assignment]
        default="ascendants_and_descendants",
        description="Controls whether to search in parent regions (ascendants), child regions (descendants), or both when filtering by brain region.",
    )


class EModelGetAllTool(BaseTool):
    """Class defining the Get EModel logic."""

    name: ClassVar[str] = "entitycore-emodel-getall"
    name_frontend: ClassVar[str] = "Get All E-Models"
    utterances: ClassVar[list[str]] = [
        "Find e-models",
        "Show me available e-models",
        "What e-models are there?",
    ]
    description: ClassVar[
        str
    ] = """Searches a neuroscience based knowledge graph to retrieve e-models.
    The output is a list of e-models, containing:
    - The e-model ID
    - The e-model name
    - The e-model description
    - The e-model iteration
    - The e-model score
    - The associated species
    - The associated brain region
    - The associated e-types and m-types
    - Creation and update dates

    We explicitly exclude the ion channel models but you can access them using the Get One E-Model tool.
    """
    description_frontend: ClassVar[
        str
    ] = """Search and retrieve e-models. Use this tool to:
    • Find e-models by name
    • Filter by brain region
    • Filter by species
    • Access detailed e-model data

    Specify optional criteria to find relevant e-models."""
    metadata: EntitycoreMetadata
    input_schema: EModelGetAllInput

    async def arun(self) -> ListResponseEModelReadExpanded:
        """Extract e-models.

        Returns
        -------
            list of e-models and their metadata, or an error dict.
        """
        query_params = self.input_schema.model_dump(exclude_defaults=True, mode="json")
        query_params["page_size"] = self.input_schema.page_size
        query_params["within_brain_region_direction"] = (
            self.input_schema.within_brain_region_direction
        )

        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = str(self.metadata.vlab_id)
        if self.metadata.project_id is not None:
            headers["project-id"] = str(self.metadata.project_id)

        # Retrieve the hierarchy_id from br_id
        if self.input_schema.within_brain_region_brain_region_id is not None:
            brain_region = await self.metadata.httpx_client.get(
                url=self.metadata.entitycore_url.rstrip("/")
                + f"/brain-region/{self.input_schema.within_brain_region_brain_region_id}",
                headers=headers,
            )
            if brain_region.status_code != 200:
                raise ValueError(
                    f"The Brain Region endpoint returned a non 200 response code. Error: {brain_region.text}"
                )
            query_params["within_brain_region_hierarchy_id"] = brain_region.json()[
                "hierarchy_id"
            ]

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/") + "/emodel",
            headers=headers,
            params=query_params,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The emodel endpoint returned a non 200 response code. Error: {response.text}"
            )

        response_data = response.json()

        # Modify the data in place
        for emodel in response_data["data"]:
            emodel["assets"] = []
            emodel["ion_channel_models"] = []

            # Add the links to website
            emodel["url_link"] = self.metadata.entity_frontend_url + "/" + emodel["id"]

        return ListResponseEModelReadExpanded(**response_data)

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

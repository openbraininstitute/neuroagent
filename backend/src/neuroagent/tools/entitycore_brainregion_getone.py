"""Get One Brain Region tool."""

from typing import ClassVar
from uuid import UUID

from httpx import AsyncClient
from pydantic import BaseModel, Field

from neuroagent.tools.autogenerated_types.entitycore import (
    BrainRegionRead,
)
from neuroagent.tools.base_tool import BaseTool, EntitycoreMetadata


class BrainRegionGetOneInput(BaseModel):
    """Inputs of the knowledge graph API."""

    brainregion_id: UUID = Field(
        description="ID of the brain region of interest in UUID format."
    )


class BrainRegionGetOneTool(BaseTool):
    """Class defining the Get One Brain Region logic."""

    name: ClassVar[str] = "entitycore-brainregion-getone"
    name_frontend: ClassVar[str] = "Get One Brain Region"
    description: ClassVar[
        str
    ] = """Retrieves detailed information about a specific brain region from the knowledge graph.
    Requires a 'brainregion_id' which is the ID of the brain region of interest as registered in the knowledge graph.
    The output contains detailed information about the brain region, including:
    - The brain region ID
    - The brain region name
    - The brain region acronym
    - The brain region annotation value
    - The brain region color hex triplet
    - The brain region hierarchy information
    - Creation and update dates
    """
    description_frontend: ClassVar[
        str
    ] = """Get detailed information about a specific brain region. Use this tool to:
    • View complete brain region details
    • Access brain region hierarchy information
    • Get creation and update information

    Specify the brain region ID to retrieve its full details."""
    metadata: EntitycoreMetadata
    input_schema: BrainRegionGetOneInput

    async def arun(self) -> BrainRegionRead:
        """From a brain region ID, extract detailed brain region information.

        Returns
        -------
            BrainRegionRead containing detailed brain region information, or an error dict.
        """
        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = str(self.metadata.vlab_id)
        if self.metadata.project_id is not None:
            headers["project-id"] = str(self.metadata.project_id)

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/")
            + f"/brain-region/{self.input_schema.brainregion_id}",
            headers=headers,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The brain-region endpoint returned a non 200 response code. Error: {response.text}"
            )
        return BrainRegionRead(**response.json())

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

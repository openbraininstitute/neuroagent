"""Get One SingleNeuronSimulation tool."""

from typing import ClassVar
from uuid import UUID

from httpx import AsyncClient
from pydantic import BaseModel, Field

from neuroagent.tools.autogenerated_types.entitycore import (
    SingleNeuronSimulationRead,
)
from neuroagent.tools.base_tool import BaseTool, EntitycoreMetadata


class SingleNeuronSimulationGetOneInput(BaseModel):
    """Inputs for the single-neuron-simulation get one tool."""

    single_neuron_simulation_id: UUID = Field(
        description="ID of the single-neuron-simulation of interest in UUID format."
    )


class SingleNeuronSimulationGetOneTool(BaseTool):
    """Class defining the Get One SingleNeuronSimulation logic."""

    name: ClassVar[str] = "entitycore-singleneuronsimulation-getone"
    name_frontend: ClassVar[str] = "Get One Single Neuron Simulation"
    utterances: ClassVar[list[str]] = [
        "Get details for this single neuron simulation",
        "Show me information about this single neuron simulation",
        "What are the properties of this single neuron simulation?",
    ]
    description: ClassVar[
        str
    ] = """Retrieves detailed information about a specific single-neuron-simulation from the knowledge graph.
    Requires a 'single_neuron_simulation_id' which is the ID of the single-neuron-simulation of interest as registered in the knowledge graph.
    The output contains detailed information about the single-neuron-simulation, including:
    - The single neuron simulation ID and basic identifiers
    - Core single neuron simulation information and properties
    - Simulation parameters and configuration
    - Creation and modification timestamps
    - Creation and update dates
    """
    description_frontend: ClassVar[
        str
    ] = """Get detailed information about a specific single-neuron-simulation. Use this tool to:
    • View complete single-neuron-simulation details
    • Access detailed single-neuron-simulation information
    • Get creation and update information

    Specify the single-neuron-simulation ID to retrieve its full details."""
    metadata: EntitycoreMetadata
    input_schema: SingleNeuronSimulationGetOneInput

    async def arun(self) -> SingleNeuronSimulationRead:
        """From a single-neuron-simulation ID, extract detailed single-neuron-simulation information.

        Returns
        -------
            SingleNeuronSimulationRead containing detailed single-neuron-simulation information, or an error dict.
        """
        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = str(self.metadata.vlab_id)
        if self.metadata.project_id is not None:
            headers["project-id"] = str(self.metadata.project_id)

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/")
            + f"/single-neuron-simulation/{self.input_schema.single_neuron_simulation_id}",
            headers=headers,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The single-neuron-simulation endpoint returned a non 200 response code. Error: {response.text}"
            )
        return SingleNeuronSimulationRead(**response.json())

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

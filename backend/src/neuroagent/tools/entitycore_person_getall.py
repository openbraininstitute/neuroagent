"""Get All Person tool."""

from typing import ClassVar

from httpx import AsyncClient
from pydantic import Field

from neuroagent.tools.autogenerated_types.entitycore import (
    ListResponsePersonRead,
    ReadManyPersonGetParametersQuery,
)
from neuroagent.tools.base_tool import BaseMetadata, BaseTool


class PersonGetAllInput(ReadManyPersonGetParametersQuery):
    """Inputs for the person get all tool."""

    page_size: int = Field(
        ge=1,
        le=10,
        default=5,
        description="Number of items per page",
    )


class PersonGetAllMetadata(BaseMetadata):
    """Metadata class for PersonGetAllTool."""

    httpx_client: AsyncClient
    entitycore_url: str
    vlab_id: str | None
    project_id: str | None


class PersonGetAllTool(BaseTool):
    """Class defining the Get All Person logic."""

    name: ClassVar[str] = "entitycore-person-getall"
    name_frontend: ClassVar[str] = "Get All Persons"
    description: ClassVar[
        str
    ] = """Searches a neuroscience based knowledge graph to retrieve persons.
    The output is a list of persons, containing:
    - The person ID
    - The person's given name
    - The person's family name
    - The person's preferred label
    """
    description_frontend: ClassVar[
        str
    ] = """Search and retrieve persons. Use this tool to:
    • Find persons by name
    • Access detailed person data

    Specify optional criteria to find relevant persons."""
    metadata: PersonGetAllMetadata
    input_schema: PersonGetAllInput

    async def arun(self) -> ListResponsePersonRead:
        """Extract persons.

        Returns
        -------
            list of persons and their metadata, or an error dict.
        """
        query_params = self.input_schema.model_dump(exclude_defaults=True, mode="json")
        query_params["page_size"] = self.input_schema.page_size

        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = self.metadata.vlab_id
        if self.metadata.project_id is not None:
            headers["project-id"] = self.metadata.project_id

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/") + "/person",
            headers=headers,
            params=query_params,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The person endpoint returned a non 200 response code. Error: {response.text}"
            )

        response_data = response.json()
        return ListResponsePersonRead(**response_data)

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

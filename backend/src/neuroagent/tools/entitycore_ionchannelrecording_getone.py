"""Get One Ion Channel Recording tool."""

from typing import ClassVar
from uuid import UUID

from httpx import AsyncClient
from pydantic import BaseModel, Field

from neuroagent.tools.autogenerated_types.entitycore import (
    IonChannelRecordingRead,
)
from neuroagent.tools.base_tool import BaseTool, EntitycoreMetadata


class IonChannelRecordingGetOneInput(BaseModel):
    """Inputs for the ion-channel-recording get one tool."""

    ion_channel_recording_id: UUID = Field(
        description="ID of the ion-channel-recording of interest in UUID format."
    )


class IonChannelRecordingGetOneTool(BaseTool):
    """Class defining the Get One IonChannelRecording logic."""

    name: ClassVar[str] = "entitycore-ionchannelrecording-getone"
    name_frontend: ClassVar[str] = "Get One Ion Channel Recording"
    description: ClassVar[
        str
    ] = """Retrieves detailed information about a specific ion-channel-recording from the knowledge graph.
    Requires a 'ion_channel_recording_id' which is the ID of the ion-channel-recording of interest as registered in the knowledge graph.
    The output contains detailed information about the ion-channel-recording, including:
    - The ion channel recording ID and basic identifiers
    - Core ion channel recording information and properties
    - Status and state information
    - Associated metadata and relationships
    - Creation and modification timestamps
    - Creation and update dates
    """
    description_frontend: ClassVar[
        str
    ] = """Get detailed information about a specific ion-channel-recording. Use this tool to:
    • View complete ion-channel-recording details
    • Access detailed ion-channel-recording information
    • Get creation and update information

    Specify the ion-channel-recording ID to retrieve its full details."""
    metadata: EntitycoreMetadata
    input_schema: IonChannelRecordingGetOneInput

    async def arun(self) -> IonChannelRecordingRead:
        """From a ion-channel-recording ID, extract detailed ion-channel-recording information.

        Returns
        -------
            IonChannelRecordingRead containing detailed ion-channel-recording information, or an error dict.
        """
        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = str(self.metadata.vlab_id)
        if self.metadata.project_id is not None:
            headers["project-id"] = str(self.metadata.project_id)

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/")
            + f"/ion-channel-recording/{self.input_schema.ion_channel_recording_id}",
            headers=headers,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The ion-channel-recording endpoint returned a non 200 response code. Error: {response.text}"
            )
        return IonChannelRecordingRead(**response.json())

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

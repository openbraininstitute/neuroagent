"""Get One Brain Atlas tool."""

from typing import ClassVar
from uuid import UUID

from httpx import AsyncClient
from pydantic import BaseModel, Field

from neuroagent.tools.autogenerated_types.entitycore import (
    BrainAtlasRead,
)
from neuroagent.tools.base_tool import BaseMetadata, BaseTool


class BrainAtlasGetOneInput(BaseModel):
    """Inputs of the knowledge graph API."""

    brain_atlas_id: UUID = Field(
        description="ID of the brain atlas of interest in UUID format."
    )


class BrainAtlasGetOneMetadata(BaseMetadata):
    """Metadata class for BrainAtlasGetOneTool."""

    httpx_client: AsyncClient
    entitycore_url: str
    vlab_id: str | None
    project_id: str | None


class BrainAtlasGetOneTool(BaseTool):
    """Class defining the Get One Brain Atlas logic."""

    name: ClassVar[str] = "entitycore-brainatlas-getone"
    name_frontend: ClassVar[str] = "Get One Brain Atlas"
    description: ClassVar[
        str
    ] = """Retrieves detailed information about a specific brain atlas from the knowledge graph.
    Requires a 'brain_atlas_id' which is the ID of the brain atlas of interest as registered in the knowledge graph.
    The output contains detailed information about the brain atlas, including:
    - The brain atlas ID
    - The brain atlas name
    - The brain atlas species
    - Creation and update dates
    """
    description_frontend: ClassVar[
        str
    ] = """Get detailed information about a specific brain atlas. Use this tool to:
    • View complete brain atlas details
    • Access brain atlas name and species
    • Get creation and update information

    Specify the brain atlas ID to retrieve its full details."""
    metadata: BrainAtlasGetOneMetadata
    input_schema: BrainAtlasGetOneInput

    async def arun(self) -> BrainAtlasRead:
        """From a brain atlas ID, extract detailed brain atlas information.

        Returns
        -------
            BrainAtlasRead containing detailed brain atlas information, or an error dict.
        """
        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = self.metadata.vlab_id
        if self.metadata.project_id is not None:
            headers["project-id"] = self.metadata.project_id

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/")
            + f"/brain-atlas/{self.input_schema.brain_atlas_id}",
            headers=headers,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The brain atlas endpoint returned a non 200 response code. Error: {response.text}"
            )
        return BrainAtlasRead(**response.json())

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

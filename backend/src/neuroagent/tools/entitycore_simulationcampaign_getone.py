"""Get One SimulationCampaign tool."""

from typing import ClassVar
from uuid import UUID

from httpx import AsyncClient
from pydantic import BaseModel, Field

from neuroagent.autogenerated_types.entitycore import (
    SimulationCampaignRead,
)
from neuroagent.tools.base_tool import BaseTool, EntitycoreMetadata


class SimulationCampaignGetOneInput(BaseModel):
    """Inputs for the simulation-campaign get one tool."""

    simulation_campaign_id: UUID = Field(
        description="ID of the simulation-campaign of interest in UUID format."
    )


class SimulationCampaignGetOneTool(BaseTool):
    """Class defining the Get One SimulationCampaign logic."""

    name: ClassVar[str] = "entitycore-simulationcampaign-getone"
    name_frontend: ClassVar[str] = "Get One Simulation Campaign"
    utterances: ClassVar[list[str]] = [
        "Get details for this simulation campaign",
        "Show me information about this simulation campaign",
        "What are the properties of this simulation campaign?",
    ]
    description: ClassVar[
        str
    ] = """Retrieves detailed information about a specific simulation-campaign from the knowledge graph.
    Requires a 'simulation_campaign_id' which is the ID of the simulation-campaign of interest as registered in the knowledge graph.
    The output contains detailed information about the simulation-campaign, including:
    - The simulation campaign ID and basic identifiers
    - Core simulation campaign information and properties
    - Simulation parameters and configuration
    - Creation and modification timestamps
    - Creation and update dates
    """
    description_frontend: ClassVar[
        str
    ] = """Get detailed information about a specific simulation-campaign. Use this tool to:
    • View complete simulation-campaign details
    • Access detailed simulation-campaign information
    • Get creation and update information

    Specify the simulation-campaign ID to retrieve its full details."""
    metadata: EntitycoreMetadata
    input_schema: SimulationCampaignGetOneInput

    async def arun(self) -> SimulationCampaignRead:
        """From a simulation-campaign ID, extract detailed simulation-campaign information.

        Returns
        -------
            SimulationCampaignRead containing detailed simulation-campaign information, or an error dict.
        """
        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = str(self.metadata.vlab_id)
        if self.metadata.project_id is not None:
            headers["project-id"] = str(self.metadata.project_id)

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/")
            + f"/simulation-campaign/{self.input_schema.simulation_campaign_id}",
            headers=headers,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The simulation-campaign endpoint returned a non 200 response code. Error: {response.text}"
            )

        response_data = response.json()
        response_data["url_link"] = (
            self.metadata.entity_frontend_url + "/" + response_data["id"]
        )
        return SimulationCampaignRead(**response_data)

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

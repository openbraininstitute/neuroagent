"""Get All Circuits tool."""

from typing import ClassVar
from uuid import UUID

from httpx import AsyncClient
from pydantic import Field

from neuroagent.tools.autogenerated_types.entitycore import (
    ListResponseCircuitRead,
    ReadManyCircuitGetParametersQuery,
)
from neuroagent.tools.base_tool import (
    BaseTool,
    EntitycoreExcludeBRParams,
    EntitycoreMetadata,
)


class CircuitGetAllInput(EntitycoreExcludeBRParams, ReadManyCircuitGetParametersQuery):
    """Inputs for the circuit get all tool."""

    within_brain_region_brain_region_id: UUID | None = Field(
        default=None,
        description="ID of the brain region of interest in UUID format.",
    )
    page_size: int = Field(
        ge=1,
        le=10,
        default=5,
        description="Number of items per page",
    )


class CircuitGetAllTool(BaseTool):
    """Class defining the Get All Circuits logic."""

    name: ClassVar[str] = "entitycore-circuit-getall"
    name_frontend: ClassVar[str] = "Get All Circuits"
    utterances: ClassVar[list[str]] = [
        "Find circuits",
        "Show me available circuits",
        "What circuits are there?",
    ]
    description: ClassVar[
        str
    ] = """Searches a neuroscience based knowledge graph to retrieve circuits.

    **Important**: This tool only provides basic circuit metadata and does NOT contain detailed circuit metrics such as:
    - Node populations (biophysical or virtual neurons)
    - Edge populations (chemical or electrical connections)
    - Synaptic properties and connectivity data
    - Circuit statistics and measurements
    - Filterable properties for analysis

    The output is a list of circuits, containing:
    - The circuit ID and basic identifiers
    - Core circuit information and properties
    - Status and state information
    - Associated metadata and relationships
    - Creation and modification timestamps

    For detailed circuit analysis and metrics, use the obi-one endpoints:
    - `obione-circuitmetrics-getone` - Get comprehensive circuit metrics and properties
    - `obione-circuitconnectivitymetrics-getone` - Analyze connectivity patterns and synaptic connections
    """
    description_frontend: ClassVar[
        str
    ] = """Search and retrieve circuits. Use this tool to:
    • Find circuits by various criteria
    • Access basic circuit metadata

    Specify optional criteria to find relevant circuits."""
    metadata: EntitycoreMetadata
    input_schema: CircuitGetAllInput

    async def arun(self) -> ListResponseCircuitRead:
        """Extract circuits.

        Returns
        -------
            list of circuits and their metadata, or an error dict.
        """
        query_params = self.input_schema.model_dump(exclude_defaults=True, mode="json")
        query_params["page_size"] = self.input_schema.page_size

        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = str(self.metadata.vlab_id)
        if self.metadata.project_id is not None:
            headers["project-id"] = str(self.metadata.project_id)

        # Retrieve the hierarchy_id from br_id
        if self.input_schema.within_brain_region_brain_region_id is not None:
            brain_region = await self.metadata.httpx_client.get(
                url=self.metadata.entitycore_url.rstrip("/")
                + f"/brain-region/{self.input_schema.within_brain_region_brain_region_id}",
                headers=headers,
            )
            if brain_region.status_code != 200:
                raise ValueError(
                    f"The Brain Region endpoint returned a non 200 response code. Error: {brain_region.text}"
                )
            query_params["within_brain_region_hierarchy_id"] = brain_region.json()[
                "hierarchy_id"
            ]

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/") + "/circuit",
            headers=headers,
            params=query_params,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The circuit endpoint returned a non 200 response code. Error: {response.text}"
            )

        response_data = response.json()
        for circuit in response_data["data"]:
            circuit["assets"] = []
            circuit["contributions"] = []

            # Add the links to website
            circuit["url_link"] = (
                self.metadata.entity_frontend_url + "/" + circuit["id"]
            )

        return ListResponseCircuitRead(**response_data)

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

"""Get Brain Region Hierarchy tool."""

from typing import ClassVar

from httpx import AsyncClient

from neuroagent.tools.autogenerated_types.entitycore import (
    ListResponseBrainRegionHierarchyRead,
    ReadManyBrainRegionHierarchyGetParametersQuery,
)
from neuroagent.tools.base_tool import BaseMetadata, BaseTool


class BrainRegionHierarchyGetAllMetadata(BaseMetadata):
    """Metadata class for BrainRegionHierarchyGetAllTool."""

    httpx_client: AsyncClient
    entitycore_url: str
    vlab_id: str | None
    project_id: str | None


class BrainRegionHierarchyGetAllTool(BaseTool):
    """Class defining the Get Brain Region Hierarchy logic."""

    name: ClassVar[str] = "entitycore-brainregionhierarchy-getall"
    name_frontend: ClassVar[str] = "Get All Brain Region Hierarchies"
    description: ClassVar[
        str
    ] = """Searches a neuroscience based knowledge graph to retrieve brain region hierarchies.
    The output is a list of brain region hierarchies, containing:
    - The hierarchy ID
    - The hierarchy name
    - Creation and update dates
    """
    description_frontend: ClassVar[
        str
    ] = """Search and retrieve brain region hierarchies. Use this tool to:
    • Find brain region hierarchies by name
    • Access detailed hierarchy data

    Specify optional criteria to find relevant hierarchies."""
    metadata: BrainRegionHierarchyGetAllMetadata
    input_schema: ReadManyBrainRegionHierarchyGetParametersQuery

    async def arun(self) -> ListResponseBrainRegionHierarchyRead:
        """Extract brain region hierarchies.

        Returns
        -------
            list of brain region hierarchies and their metadata, or an error dict.
        """
        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = self.metadata.vlab_id
        if self.metadata.project_id is not None:
            headers["project-id"] = self.metadata.project_id

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/") + "/brain-region-hierarchy",
            headers=headers,
            params=self.input_schema.model_dump(exclude_defaults=True, mode="json"),
        )
        if response.status_code != 200:
            raise ValueError(
                f"The brain region hierarchy endpoint returned a non 200 response code. Error: {response.text}"
            )

        response_data = response.json()
        return ListResponseBrainRegionHierarchyRead(**response_data)

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

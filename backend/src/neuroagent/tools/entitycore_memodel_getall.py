"""Get all Me-Models."""

import logging
from typing import ClassVar, Literal
from uuid import UUID

from httpx import AsyncClient
from pydantic import Field

from neuroagent.tools.autogenerated_types.entitycore import (
    ListResponseMEModelRead,
    ReadManyMemodelGetParametersQuery,
)
from neuroagent.tools.base_tool import (
    BaseTool,
    EntitycoreExcludeBRParams,
    EntitycoreMetadata,
)

logger = logging.getLogger(__name__)


class MEModelGetAllInput(EntitycoreExcludeBRParams, ReadManyMemodelGetParametersQuery):
    """Input for the ME-Model get all tool."""

    within_brain_region_brain_region_id: UUID | None = Field(
        default=None,
        description="ID of the brain region of interest in UUID format.",
    )
    page_size: int = Field(
        ge=1,
        le=10,
        default=5,
        description="Number of items per page",
    )
    within_brain_region_direction: Literal[
        "ascendants", "descendants", "ascendants_and_descendants"
    ] = Field(  # type: ignore[assignment]
        default="ascendants_and_descendants",
        description="Controls whether to search in parent regions (ascendants), child regions (descendants), or both when filtering by brain region.",
    )


class MEModelGetAllTool(BaseTool):
    """Definition of the ME-Model get all tool."""

    name: ClassVar[str] = "entitycore-memodel-getall"
    name_frontend: ClassVar[str] = "Get all ME-Models"
    utterances: ClassVar[list[str]] = [
        "Find ME-models",
        "Show me available ME-models",
        "What ME-models are there?",
    ]
    description: ClassVar[
        str
    ] = """Search the entitycore database to find Morphological Electric models (ME-Model).
    These are models that are ready to be simulated. You will need the ID of the models to run the subsequent simulations."""
    description_frontend: ClassVar[
        str
    ] = """Searches for Morphological Electric models (ME-Model) in the entitycore database. Use this tool when you want to:
    • Find MeModels in specific brain regions
    • Filter by subject and species
    • Access detailed MeModels data before running a simulation."""
    metadata: EntitycoreMetadata
    input_schema: MEModelGetAllInput

    async def arun(self) -> ListResponseMEModelRead:
        """Get many ME-Model from the database while allowing filtering.

        Returns
        -------
            ListResponseMEModelCalibrationResultRead describing the list of me model and their metadata.
        """
        query_params = self.input_schema.model_dump(exclude_defaults=True, mode="json")
        query_params["page_size"] = self.input_schema.page_size
        query_params["within_brain_region_direction"] = (
            self.input_schema.within_brain_region_direction
        )

        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = str(self.metadata.vlab_id)
        if self.metadata.project_id is not None:
            headers["project-id"] = str(self.metadata.project_id)

        # Retrieve the hierarchy_id from br_id
        if self.input_schema.within_brain_region_brain_region_id is not None:
            brain_region = await self.metadata.httpx_client.get(
                url=self.metadata.entitycore_url.rstrip("/")
                + f"/brain-region/{self.input_schema.within_brain_region_brain_region_id}",
                headers=headers,
            )
            if brain_region.status_code != 200:
                raise ValueError(
                    f"The Brain Region endpoint returned a non 200 response code. Error: {brain_region.text}"
                )
            query_params["within_brain_region_hierarchy_id"] = brain_region.json()[
                "hierarchy_id"
            ]

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/") + "/memodel",
            headers=headers,
            params=query_params,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The ME-Model endpoint returned a non 200 response code. Error: {response.text}"
            )

        response_data = response.json()

        # Set assets and legacy_id to empty lists for each morphology and emodel.
        for memodel in response_data["data"]:
            memodel["emodel"]["assets"] = []
            memodel["emodel"]["ion_channel_models"] = []
            memodel["emodel"]["legacy_id"] = []

            # Add the links to website
            memodel["url_link"] = (
                self.metadata.entity_frontend_url + "/" + memodel["id"]
            )

        return ListResponseMEModelRead(**response_data)

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

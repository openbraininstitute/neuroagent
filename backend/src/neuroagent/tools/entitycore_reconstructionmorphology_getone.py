"""Get One Reconstruction Morphology tool."""

from typing import ClassVar
from uuid import UUID

from httpx import AsyncClient
from pydantic import BaseModel, Field

from neuroagent.tools.autogenerated_types.entitycore import (
    ReadOneReconstructionMorphologyIdGetResponse,
)
from neuroagent.tools.base_tool import BaseTool, EntitycoreMetadata


class ReconstructionMorphologyGetOneInput(BaseModel):
    """Inputs of the knowledge graph API."""

    morphology_id: UUID = Field(
        description="ID of the morphology of interest in UUID format."
    )


class ReconstructionMorphologyGetOneTool(BaseTool):
    """Class defining the Get One Reconstruction Morphology logic."""

    name: ClassVar[str] = "entitycore-reconstructionmorphology-getone"
    name_frontend: ClassVar[str] = "Get One Reconstruction Morphology"
    utterances: ClassVar[list[str]] = [
        "Get details for this reconstruction morphology",
        "Show me information about this reconstruction morphology",
        "What are the properties of this reconstruction morphology?",
    ]
    description: ClassVar[
        str
    ] = """Retrieves detailed information about a specific reconstruction morphology from the knowledge graph.
    Requires a 'morphology_id' which is the ID of the morphology of interest as registered in the knowledge graph.
    The output contains detailed information about the morphology, including:
    - The morphology ID
    - The morphology name
    - The morphology description
    - The subject information
    - The measurements
    - The assets
    - The contributions
    - The brain region information
    - The mtypes
    - Any additional metadata
    """
    description_frontend: ClassVar[
        str
    ] = """Get detailed information about a specific reconstruction morphology. Use this tool to:
    • View complete morphology details
    • Access associated metadata
    • Get brain region and subject information
    • Access morphology assets and measurements

    Specify the morphology ID to retrieve its full details."""
    metadata: EntitycoreMetadata
    input_schema: ReconstructionMorphologyGetOneInput

    async def arun(self) -> ReadOneReconstructionMorphologyIdGetResponse:
        """From a morphology ID, extract detailed morphology information.

        Returns
        -------
            ReconstructionMorphologyIdGetResponse containing detailed morphology information, or an error dict.
        """
        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = str(self.metadata.vlab_id)
        if self.metadata.project_id is not None:
            headers["project-id"] = str(self.metadata.project_id)

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/")
            + f"/reconstruction-morphology/{self.input_schema.morphology_id}",
            headers=headers,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The morphology endpoint returned a non 200 response code. Error: {response.text}"
            )
        # Add the link
        response_json = response.json()
        response_json["url_link"] = (
            self.metadata.entity_frontend_url
            + "/explore/interactive/experimental"
            + "/morphology/"
            + response_json["id"]
        )
        return ReadOneReconstructionMorphologyIdGetResponse(**response_json)

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

"""Get One Species tool."""

from typing import ClassVar
from uuid import UUID

from httpx import AsyncClient
from pydantic import BaseModel, Field

from neuroagent.tools.autogenerated_types.entitycore import (
    SpeciesRead,
)
from neuroagent.tools.base_tool import BaseTool, EntitycoreMetadata


class SpeciesGetOneInput(BaseModel):
    """Inputs of the knowledge graph API."""

    species_id: UUID = Field(
        description="ID of the species of interest in UUID format."
    )


class SpeciesGetOneTool(BaseTool):
    """Class defining the Get One Species logic."""

    name: ClassVar[str] = "entitycore-species-getone"
    name_frontend: ClassVar[str] = "Get One Species"
    description: ClassVar[
        str
    ] = """Retrieves detailed information about a specific species from the knowledge graph.
    Requires a 'species_id' which is the ID of the species of interest as registered in the knowledge graph.
    The output contains detailed information about the species, including:
    - The species ID
    - The species name
    - The species taxonomy ID
    - Creation and update dates
    """
    description_frontend: ClassVar[
        str
    ] = """Get detailed information about a specific species. Use this tool to:
    • View complete species details
    • Access species name and taxonomy ID
    • Get creation and update information

    Specify the species ID to retrieve its full details."""
    metadata: EntitycoreMetadata
    input_schema: SpeciesGetOneInput

    async def arun(self) -> SpeciesRead:
        """From a species ID, extract detailed species information.

        Returns
        -------
            SpeciesRead containing detailed species information, or an error dict.
        """
        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = str(self.metadata.vlab_id)
        if self.metadata.project_id is not None:
            headers["project-id"] = str(self.metadata.project_id)

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/")
            + f"/species/{self.input_schema.species_id}",
            headers=headers,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The species endpoint returned a non 200 response code. Error: {response.text}"
            )
        return SpeciesRead(**response.json())

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

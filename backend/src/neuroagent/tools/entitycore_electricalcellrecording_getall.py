"""Get All Electrical Cell Recordings tool."""

from typing import ClassVar
from uuid import UUID

from httpx import AsyncClient
from pydantic import Field

from neuroagent.tools.autogenerated_types.entitycore import (
    ListResponseElectricalCellRecordingRead,
    ReadManyElectricalCellRecordingGetParametersQuery,
    WithinBrainRegionDirection,
)
from neuroagent.tools.base_tool import (
    BaseTool,
    EntitycoreExcludeBRParams,
    EntitycoreMetadata,
)


class ElectricalCellRecordingGetAllInput(
    EntitycoreExcludeBRParams, ReadManyElectricalCellRecordingGetParametersQuery
):
    """Inputs for the electrical cell recording get all tool."""

    within_brain_region_brain_region_id: UUID | None = Field(
        default=None,
        description="ID of the brain region of interest in UUID format.",
    )
    page_size: int = Field(
        ge=1,
        le=10,
        default=5,
        description="Number of items per page",
    )
    within_brain_region_direction: WithinBrainRegionDirection = Field(
        default="ascendants_and_descendants", title="Within Brain Region Direction"
    )


class ElectricalCellRecordingGetAllTool(BaseTool):
    """Class defining the Get All Electrical Cell Recordings logic."""

    name: ClassVar[str] = "entitycore-electricalcellrecording-getall"
    name_frontend: ClassVar[str] = "Get All Electrical Cell Recordings"
    utterances: ClassVar[list[str]] = [
        "Find electrical cell recordings",
        "Show me available recordings",
        "What recordings are there?",
        "Find a trace in the isocortex",
        "Show me traces in this region",
    ]
    description: ClassVar[
        str
    ] = """Searches the entitycore database to retrieve electrical cell recordings.
    Often, electrical cell recordings are used interchangeably with the word 'trace'.

    The output is a list of electrical cell recordings (trace), containing:
    - The brain region ID.
    - The brain region name.
    - The subject species name.
    - The recording ID.
    - The recording name.
    - The recording description.
    - The recording type.
    - The recording origin.
    - The recording location.
    - The liquid junction potential.
    - Any additional metadata.

    We explicitly exclude the legacy id but you can access it using the Get One Electrical Cell Recording tool.
    """
    description_frontend: ClassVar[
        str
    ] = """Search and retrieve electrical cell recordings. Use this tool to:
    • Find recordings in specific brain regions
    • Filter by recording type and origin
    • Access detailed recording data

    Specify brain region and optional criteria to find relevant recordings."""
    metadata: EntitycoreMetadata
    input_schema: ElectricalCellRecordingGetAllInput

    async def arun(self) -> ListResponseElectricalCellRecordingRead:
        """From a brain region ID, extract electrical cell recordings.

        Returns
        -------
            list of ElectricalCellRecordingRead to describe the recordings and their metadata, or an error dict.
        """
        query_params = self.input_schema.model_dump(exclude_defaults=True, mode="json")
        query_params["page_size"] = self.input_schema.page_size

        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = str(self.metadata.vlab_id)
        if self.metadata.project_id is not None:
            headers["project-id"] = str(self.metadata.project_id)

        # Retrieve the hierarchy_id from br_id
        if self.input_schema.within_brain_region_brain_region_id is not None:
            brain_region = await self.metadata.httpx_client.get(
                url=self.metadata.entitycore_url.rstrip("/")
                + f"/brain-region/{self.input_schema.within_brain_region_brain_region_id}",
                headers=headers,
            )
            if brain_region.status_code != 200:
                raise ValueError(
                    f"The Brain Region endpoint returned a non 200 response code. Error: {brain_region.text}"
                )
            query_params["within_brain_region_hierarchy_id"] = brain_region.json()[
                "hierarchy_id"
            ]

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/") + "/electrical-cell-recording",
            headers=headers,
            params=query_params,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The electrical cell recording endpoint returned a non 200 response code. Error: {response.text}"
            )

        response_data = response.json()
        # Set assets and legacy_id to empty lists for each recording
        for recording in response_data["data"]:
            recording["legacy_id"] = []

            # Add the links to website
            recording["url_link"] = (
                self.metadata.entity_frontend_url + "/" + recording["id"]
            )

        return ListResponseElectricalCellRecordingRead(**response_data)

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

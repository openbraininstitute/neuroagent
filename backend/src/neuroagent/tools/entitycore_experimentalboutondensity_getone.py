"""Get One Experimental Bouton Density tool."""

import logging
from typing import ClassVar
from uuid import UUID

from httpx import AsyncClient
from pydantic import BaseModel, Field

from neuroagent.tools.autogenerated_types.entitycore import (
    ExperimentalBoutonDensityRead,
)
from neuroagent.tools.base_tool import BaseTool, EntitycoreMetadata

logger = logging.getLogger(__name__)


class ExperimentalBoutonDensityGetOneInput(BaseModel):
    """Inputs of the experimental bouton density get one tool."""

    bouton_density_id: UUID = Field(
        description="ID of the experimental bouton density of interest in UUID format."
    )


class ExperimentalBoutonDensityGetOneTool(BaseTool):
    """Class defining the Get One Experimental Bouton Density logic."""

    name: ClassVar[str] = "entitycore-experimentalboutondensity-getone"
    name_frontend: ClassVar[str] = "Get One Experimental Bouton Density"
    description: ClassVar[
        str
    ] = """Retrieves detailed information about a specific experimental bouton density from the knowledge graph.
    Requires a 'bouton_density_id' which is the ID of the bouton density of interest as registered in the knowledge graph.
    The output contains detailed information about the bouton density, including:
    - The bouton density ID
    - The bouton density name
    - The bouton density description
    - The subject information
    - The measurements
    - The assets
    - The contributions
    - The brain region information
    - The mtypes
    - Any additional metadata
    """
    description_frontend: ClassVar[
        str
    ] = """Get detailed information about a specific experimental bouton density. Use this tool to:
    • View complete bouton density details
    • Access associated metadata
    • Get brain region and subject information
    • Access bouton density assets and measurements

    Specify the bouton density ID to retrieve its full details."""
    metadata: EntitycoreMetadata
    input_schema: ExperimentalBoutonDensityGetOneInput

    async def arun(self) -> ExperimentalBoutonDensityRead:
        """From a bouton density ID, extract detailed bouton density information.

        Returns
        -------
            ExperimentalBoutonDensityRead containing detailed bouton density information, or an error dict.
        """
        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = str(self.metadata.vlab_id)
        if self.metadata.project_id is not None:
            headers["project-id"] = str(self.metadata.project_id)

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/")
            + f"/experimental-bouton-density/{self.input_schema.bouton_density_id}",
            headers=headers,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The experimental bouton density endpoint returned a non 200 response code. Error: {response.text}"
            )
        return ExperimentalBoutonDensityRead(**response.json())

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

"""Get One SingleNeuronSynaptomeSimulation tool."""

from typing import ClassVar
from uuid import UUID

from httpx import AsyncClient
from pydantic import BaseModel, Field

from neuroagent.autogenerated_types.entitycore import (
    SingleNeuronSynaptomeSimulationRead,
)
from neuroagent.tools.base_tool import BaseTool, EntitycoreMetadata


class SingleNeuronSynaptomeSimulationGetOneInput(BaseModel):
    """Inputs for the single-neuron-synaptome-simulation get one tool."""

    single_neuron_synaptome_simulation_id: UUID = Field(
        description="ID of the single-neuron-synaptome-simulation of interest in UUID format."
    )


class SingleNeuronSynaptomeSimulationGetOneTool(BaseTool):
    """Class defining the Get One SingleNeuronSynaptomeSimulation logic."""

    name: ClassVar[str] = "entitycore-singleneuronsynaptomesimulation-getone"
    name_frontend: ClassVar[str] = "Get One Single Neuron Synaptome Simulation"
    utterances: ClassVar[list[str]] = [
        "Get details for this single neuron synaptome simulation",
        "Show me information about this single neuron synaptome simulation",
        "What are the properties of this single neuron synaptome simulation?",
    ]
    description: ClassVar[
        str
    ] = """Retrieves detailed information about a specific single-neuron-synaptome-simulation from the knowledge graph.
    Requires a 'single_neuron_synaptome_simulation_id' which is the ID of the single-neuron-synaptome-simulation of interest as registered in the knowledge graph.
    The output contains detailed information about the single-neuron-synaptome-simulation, including:
    - The single neuron synaptome simulation ID and basic identifiers
    - Core single neuron synaptome simulation information and properties
    - Simulation parameters and configuration
    - Creation and modification timestamps
    - Creation and update dates
    """
    description_frontend: ClassVar[
        str
    ] = """Get detailed information about a specific single-neuron-synaptome-simulation. Use this tool to:
    • View complete single-neuron-synaptome-simulation details
    • Access detailed single-neuron-synaptome-simulation information
    • Get creation and update information

    Specify the single-neuron-synaptome-simulation ID to retrieve its full details."""
    metadata: EntitycoreMetadata
    input_schema: SingleNeuronSynaptomeSimulationGetOneInput

    async def arun(self) -> SingleNeuronSynaptomeSimulationRead:
        """From a single-neuron-synaptome-simulation ID, extract detailed single-neuron-synaptome-simulation information.

        Returns
        -------
            SingleNeuronSynaptomeSimulationRead containing detailed single-neuron-synaptome-simulation information, or an error dict.
        """
        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = str(self.metadata.vlab_id)
        if self.metadata.project_id is not None:
            headers["project-id"] = str(self.metadata.project_id)

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/")
            + f"/single-neuron-synaptome-simulation/{self.input_schema.single_neuron_synaptome_simulation_id}",
            headers=headers,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The single-neuron-synaptome-simulation endpoint returned a non 200 response code. Error: {response.text}"
            )

        response_data = response.json()
        response_data["url_link"] = (
            self.metadata.entity_frontend_url + "/" + response_data["id"]
        )
        return SingleNeuronSynaptomeSimulationRead(**response_data)

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

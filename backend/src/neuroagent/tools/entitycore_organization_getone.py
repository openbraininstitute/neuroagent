"""Get One Organization tool."""

from typing import ClassVar
from uuid import UUID

from httpx import AsyncClient
from pydantic import BaseModel, Field

from neuroagent.tools.autogenerated_types.entitycore import (
    OrganizationRead,
)
from neuroagent.tools.base_tool import BaseMetadata, BaseTool


class OrganizationGetOneInput(BaseModel):
    """Inputs of the knowledge graph API."""

    organization_id: str = Field(
        description="ID of the organization of interest in UUID format."
    )


class OrganizationGetOneMetadata(BaseMetadata):
    """Metadata class for OrganizationGetOneTool."""

    httpx_client: AsyncClient
    entitycore_url: str
    vlab_id: str | None
    project_id: str | None


class OrganizationGetOneTool(BaseTool):
    """Class defining the Get One Organization logic."""

    name: ClassVar[str] = "entitycore-organization-getone"
    name_frontend: ClassVar[str] = "Get One Organization"
    description: ClassVar[
        str
    ] = """Retrieves detailed information about a specific organization from the knowledge graph.
    Requires an 'organization_id' which is the ID of the organization of interest as registered in the knowledge graph.
    The output contains detailed information about the organization, including:
    - The organization ID
    - The organization's preferred label
    - The organization's alternative name
    - Creation and update dates
    """
    description_frontend: ClassVar[
        str
    ] = """Get detailed information about a specific organization. Use this tool to:
    • View complete organization details
    • Access organization names and labels
    • Get creation and update information

    Specify the organization ID to retrieve its full details."""
    metadata: OrganizationGetOneMetadata
    input_schema: OrganizationGetOneInput

    async def arun(self) -> OrganizationRead:
        """From an organization ID, extract detailed organization information.

        Returns
        -------
            OrganizationRead containing detailed organization information, or an error dict.
        """
        # Validate the UUID format
        organization_id = UUID(self.input_schema.organization_id)

        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = self.metadata.vlab_id
        if self.metadata.project_id is not None:
            headers["project-id"] = self.metadata.project_id

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/")
            + f"/organization/{organization_id}",
            headers=headers,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The organization endpoint returned a non 200 response code. Error: {response.text}"
            )
        return OrganizationRead(**response.json())

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

"""Get All Organizations tool."""

from typing import ClassVar

from httpx import AsyncClient
from pydantic import Field

from neuroagent.tools.autogenerated_types.entitycore import (
    ListResponseOrganizationRead,
    ReadManyOrganizationGetParametersQuery,
)
from neuroagent.tools.base_tool import BaseTool, EntitycoreMetadata


class OrganizationGetAllInput(ReadManyOrganizationGetParametersQuery):
    """Inputs for the organization get all tool."""

    page_size: int = Field(
        ge=1,
        le=10,
        default=5,
        description="Number of items per page",
    )


class OrganizationGetAllTool(BaseTool):
    """Class defining the Get All Organization logic."""

    name: ClassVar[str] = "entitycore-organization-getall"
    name_frontend: ClassVar[str] = "Get All Organizations"
    description: ClassVar[
        str
    ] = """Searches a neuroscience based knowledge graph to retrieve organizations.
    The output is a list of organizations, containing:
    - The organization ID
    - The organization's preferred label
    - The organization's alternative name
    """
    description_frontend: ClassVar[
        str
    ] = """Search and retrieve organizations. Use this tool to:
    • Find organizations by name
    • Access detailed organization data

    Specify optional criteria to find relevant organizations."""
    metadata: EntitycoreMetadata
    input_schema: OrganizationGetAllInput

    async def arun(self) -> ListResponseOrganizationRead:
        """Extract organizations.

        Returns
        -------
            list of organizations and their metadata, or an error dict.
        """
        query_params = self.input_schema.model_dump(exclude_defaults=True, mode="json")
        query_params["page_size"] = self.input_schema.page_size

        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = str(self.metadata.vlab_id)
        if self.metadata.project_id is not None:
            headers["project-id"] = str(self.metadata.project_id)

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/") + "/organization",
            headers=headers,
            params=query_params,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The organization endpoint returned a non 200 response code. Error: {response.text}"
            )

        response_data = response.json()
        return ListResponseOrganizationRead(**response_data)

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

"""Get One Strain tool."""

from typing import ClassVar
from uuid import UUID

from httpx import AsyncClient
from pydantic import BaseModel, Field

from neuroagent.tools.autogenerated_types.entitycore import (
    StrainRead,
)
from neuroagent.tools.base_tool import BaseMetadata, BaseTool


class StrainGetOneInput(BaseModel):
    """Inputs of the knowledge graph API."""

    strain_id: str = Field(description="ID of the strain of interest in UUID format.")


class StrainGetOneMetadata(BaseMetadata):
    """Metadata class for StrainGetOneTool."""

    httpx_client: AsyncClient
    entitycore_url: str
    vlab_id: str | None
    project_id: str | None


class StrainGetOneTool(BaseTool):
    """Class defining the Get One Strain logic."""

    name: ClassVar[str] = "entitycore-strain-getone"
    name_frontend: ClassVar[str] = "Get One Strain"
    description: ClassVar[
        str
    ] = """Retrieves detailed information about a specific strain from the knowledge graph.
    Requires a 'strain_id' which is the ID of the strain of interest as registered in the knowledge graph.
    The output contains detailed information about the strain, including:
    - The strain ID
    - The strain name
    - The strain taxonomy ID
    - The species ID
    - Creation and update dates
    """
    description_frontend: ClassVar[
        str
    ] = """Get detailed information about a specific strain. Use this tool to:
    • View complete strain details
    • Access strain name and taxonomy information
    • Get creation and update information

    Specify the strain ID to retrieve its full details."""
    metadata: StrainGetOneMetadata
    input_schema: StrainGetOneInput

    async def arun(self) -> StrainRead:
        """From a strain ID, extract detailed strain information.

        Returns
        -------
            StrainRead containing detailed strain information, or an error dict.
        """
        # Validate the UUID format
        strain_id = UUID(self.input_schema.strain_id)

        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = self.metadata.vlab_id
        if self.metadata.project_id is not None:
            headers["project-id"] = self.metadata.project_id

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/") + f"/strain/{strain_id}",
            headers=headers,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The strain endpoint returned a non 200 response code. Error: {response.text}"
            )
        return StrainRead(**response.json())

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

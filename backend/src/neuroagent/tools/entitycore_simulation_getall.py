"""Get All Simulation tool."""

from typing import ClassVar, Literal
from uuid import UUID

from httpx import AsyncClient
from pydantic import Field

from neuroagent.tools.autogenerated_types.entitycore import (
    ListResponseSimulationRead,
    ReadManySimulationGetParametersQuery,
)
from neuroagent.tools.base_tool import (
    BaseTool,
    EntitycoreExcludeBRParams,
    EntitycoreMetadata,
)


class SimulationGetAllInput(
    EntitycoreExcludeBRParams, ReadManySimulationGetParametersQuery
):
    """Inputs for the simulation get all tool."""

    within_brain_region_brain_region_id: UUID | None = Field(
        default=None,
        description="ID of the brain region of interest in UUID format.",
    )
    page_size: int = Field(
        ge=1,
        le=10,
        default=5,
        description="Number of items per page",
    )
    within_brain_region_direction: Literal[
        "ascendants", "descendants", "ascendants_and_descendants"
    ] = Field(  # type: ignore[assignment]
        default="ascendants_and_descendants", title="Within Brain Region Direction"
    )


class SimulationGetAllTool(BaseTool):
    """Class defining the Get All Simulations logic."""

    name: ClassVar[str] = "entitycore-simulation-getall"
    name_frontend: ClassVar[str] = "Get All Simulations"
    utterances: ClassVar[list[str]] = [
        "Find simulations",
        "Show me available simulations",
        "What simulations are there?",
    ]
    description: ClassVar[
        str
    ] = """Searches a neuroscience based knowledge graph to retrieve simulations.
    The output is a list of simulations, containing:
    - The simulation ID and basic identifiers
    - Core simulation information and properties
    - Simulation parameters and configuration
    - Creation and modification timestamps
    """
    description_frontend: ClassVar[
        str
    ] = """Search and retrieve simulations. Use this tool to:
    • Find simulations by various criteria
    • Access detailed simulation data

    Specify optional criteria to find relevant simulations."""
    metadata: EntitycoreMetadata
    input_schema: SimulationGetAllInput

    async def arun(self) -> ListResponseSimulationRead:
        """Extract simulations.

        Returns
        -------
            list of simulations and their metadata, or an error dict.
        """
        query_params = self.input_schema.model_dump(exclude_defaults=True, mode="json")
        query_params["page_size"] = self.input_schema.page_size
        query_params["within_brain_region_direction"] = (
            self.input_schema.within_brain_region_direction
        )

        headers: dict[str, str] = {}
        if self.metadata.vlab_id is not None:
            headers["virtual-lab-id"] = str(self.metadata.vlab_id)
        if self.metadata.project_id is not None:
            headers["project-id"] = str(self.metadata.project_id)

        # Retrieve the hierarchy_id from br_id
        if self.input_schema.within_brain_region_brain_region_id is not None:
            brain_region = await self.metadata.httpx_client.get(
                url=self.metadata.entitycore_url.rstrip("/")
                + f"/brain-region/{self.input_schema.within_brain_region_brain_region_id}",
                headers=headers,
            )
            if brain_region.status_code != 200:
                raise ValueError(
                    f"The Brain Region endpoint returned a non 200 response code. Error: {brain_region.text}"
                )
            query_params["within_brain_region_hierarchy_id"] = brain_region.json()[
                "hierarchy_id"
            ]

        response = await self.metadata.httpx_client.get(
            url=self.metadata.entitycore_url.rstrip("/") + "/simulation",
            headers=headers,
            params=query_params,
        )
        if response.status_code != 200:
            raise ValueError(
                f"The simulation endpoint returned a non 200 response code. Error: {response.text}"
            )

        response_data = response.json()
        for simulation in response_data["data"]:
            simulation["assets"] = []
        return ListResponseSimulationRead(**response_data)

    @classmethod
    async def is_online(cls, *, httpx_client: AsyncClient, entitycore_url: str) -> bool:
        """Check if the tool is online."""
        response = await httpx_client.get(
            f"{entitycore_url.rstrip('/')}/health",
        )
        return response.status_code == 200

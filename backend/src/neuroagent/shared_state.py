"""Shared state types."""

import re
from typing import Any

from pydantic import BaseModel

from neuroagent.autogenerated_types.obione import CircuitSimulationScanConfig

# Mapping from shared state keys to URL regex patterns where they are allowed.
STATE_KEY_ALLOWED_PAGES: dict[str, re.Pattern[str]] = {
    "smc_simulation_config": re.compile(
        r"^/app/virtual-lab/[^/]+/[^/]+/workflows/simulate/configure/circuit/[^/]+\?(?=.*(?:^|&)dataType=(small_micro_circuit|paired_neuron_circuit)(?:&|$))"
    ),
}


def check_state_key_page_access(state_key: str, current_frontend_url: str) -> bool:
    """Check if a state key is allowed to be accessed from the given page URL.

    Keys not present in STATE_KEY_ALLOWED_PAGES are considered global and
    always allowed (returns True).

    Parameters
    ----------
    state_key : str
        The shared state key being accessed (e.g. "smc_simulation_config").
    current_frontend_url : str
        The current frontend URL the user is on.

    Returns
    -------
    bool
        True if the state key is allowed on the current page, False otherwise.
    """
    pattern = STATE_KEY_ALLOWED_PAGES.get(state_key)
    if pattern is None:
        return True
    return pattern.search(current_frontend_url) is not None


class SharedStateStrict(BaseModel):
    """Eventually for saving to DBs."""

    smc_simulation_config: CircuitSimulationScanConfig


class SharedStatePartial(BaseModel):
    """For validation and schema dumping."""

    smc_simulation_config: CircuitSimulationScanConfig | None = None


class SharedStateLoosened(BaseModel):
    """For request body and tool outputs."""

    smc_simulation_config: dict[str, Any] | None = None
